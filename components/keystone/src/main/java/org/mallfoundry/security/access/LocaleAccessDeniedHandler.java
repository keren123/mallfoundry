package org.mallfoundry.security.access;

import org.springframework.http.HttpStatus;
import org.springframework.security.access.AccessDeniedException;
import org.springframework.security.web.access.AccessDeniedHandler;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;
import java.util.Objects;

public class LocaleAccessDeniedHandler implements AccessDeniedHandler {

    @Override
    public void handle(HttpServletRequest request, HttpServletResponse response, AccessDeniedException ex) throws IOException {
        if (!response.isCommitted()) {
            var message = Objects.requireNonNull(ex.getMessage(), HttpStatus.FORBIDDEN::getReasonPhrase);
            response.sendError(HttpStatus.FORBIDDEN.value(), message);
        }
    }
}
