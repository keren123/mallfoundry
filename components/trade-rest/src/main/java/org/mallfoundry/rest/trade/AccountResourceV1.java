package org.mallfoundry.rest.trade;

import org.mallfoundry.trade.Account;
import org.mallfoundry.trade.AccountService;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequestMapping("/v1")
public class AccountResourceV1 {

    private final AccountService accountService;

    public AccountResourceV1(AccountService accountService) {
        this.accountService = accountService;
    }

    @GetMapping("/accounts/{account_id}")
    public Account getAccount(@PathVariable(name = "account_id") String accountId) {
        return this.accountService.getAccount(accountId);
    }
}
